package Sept.ex_16092024;

public class lab024_arthematic {
    public static void main(String[] args) {
        // 2. arithematic operator ->used to perform common mathematical operations, These operators consist of various unary and binary operators that can be applied on a single or two operands.
        // ex - a and b these are 2 operands i.e why they are binary
        int a = 10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        // we also have '%' operator called as modulus and it gives the reminder of the division
        System.out.println(a%b);
    }
}
