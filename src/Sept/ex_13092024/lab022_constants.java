package Sept.ex_13092024;

public class lab022_constants {
    //Constants - > constants are variable whose value cannot be changed once they are assigned.
    // final -> final data type CONSTANT NAME = value;
    // final forces the variable to have one type of value and final is a keyword
    public static void main(String[] args) {
        final float PIE;
        PIE =3.14f;
        System.out.println(PIE);
    }
}
